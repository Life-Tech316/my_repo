from gtts import gTTS
import os

# 変換したいテキスト
text = '''こんにちは。これは音声データのサンプルです。Pythonを使ってテキストを音声に変換する方法を紹介します。
このプロセスは、様々な用途に利用できます。例えば、オーディオブックの作成、視覚障害者のための読み上げソフトウェア、
または学習資料としての活用などです。このサンプルでは、GoogleのgTTSライブラリを使用しています。gTTSは、
Google Text-to-Speechの略で、テキストを音声に変換するための強力なツールです。このライブラリを使うと、
簡単にテキストを音声に変換できます。次に、この音声をファイルとして保存し、再生する手順を見ていきましょう。
このプロセスは、非常にシンプルで、数行のコードで実現できます。まず、gTTSライブラリをインストールし、
変換したいテキストを用意します。次に、gTTSオブジェクトを作成し、そのオブジェクトを使って音声ファイルを保存します。
最後に、保存した音声ファイルを再生するだけです。この方法を使えば、誰でも簡単にテキストを音声に変換し、
さまざまな用途に活用することができます。それでは、早速やってみましょう。'''

# gTTSオブジェクトを作成
tts = gTTS(text=text, lang='ja')

# 音声ファイルを保存
tts.save("sample.mp3")

# 音声ファイルを再生（オプション）
os.system("start sample.mp3")
